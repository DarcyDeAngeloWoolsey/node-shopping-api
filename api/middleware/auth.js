//this middleware is for the purpose of verifying a user token so they can have access to protected endpoints
//if jwt.verify succeeds, then it will send the token back decoded
//headers.authorization split bearer by white space
const jwt = require('jsonwebtoken');

module.exports = (req, res, next) => {
    try {
        const token = req.headers.authorization.split(" ")[1];
        console.log(token);
        const decoded = jwt.verify(token, process.env.JWT_KEY);
        req.userData = decoded;
        next();
    } catch (error) {
        return res.status(401).json({
            message: 'Auth failed'
        });
    }
};